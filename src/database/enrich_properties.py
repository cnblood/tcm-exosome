def enrich_properties():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    updated = 0
    for name_cn, (nature, flavor, meridian, functions, active_compounds) in HERB_PROPERTIES.items():
        result = c.execute("""
            UPDATE tcm_single_herb
            SET nature=?, flavor=?, meridian=?, functions=?, active_compounds=?, data_level='enriched'
            WHERE name_cn=?
        """, (nature, flavor, meridian, functions, active_compounds, name_cn))
        if result.rowcount > 0:
            updated += result.rowcount

    conn.commit()

    total = c.execute("SELECT COUNT(*) FROM tcm_single_herb").fetchone()[0]
    enriched = c.execute("SELECT COUNT(*) FROM tcm_single_herb WHERE data_level='enriched'").fetchone()[0]
    aligned = c.execute("SELECT COUNT(*) FROM tcm_single_herb WHERE data_level='aligned'").fetchone()[0]
    skeleton = c.execute("SELECT COUNT(*) FROM tcm_single_herb WHERE data_level='skeleton'").fetchone()[0]

    print(f"总药材: {total}")
    print(f"enriched层级: {enriched} ({enriched*100//total}%)")
    print(f"aligned层级: {aligned} ({aligned*100//total}%)")
    print(f"skeleton层级: {skeleton} ({skeleton*100//total}%)")
    print(f"本次更新: {updated} 条")
    conn.close()

    if SUPABASE_URL and SUPABASE_KEY:
        from supabase import create_client
        client = create_client(SUPABASE_URL, SUPABASE_KEY)
        data = []
        for name_cn, (nature, flavor, meridian, functions, active_compounds) in HERB_PROPERTIES.items():
            data.append({
                "name_cn": name_cn,
                "nature": nature,
                "flavor": flavor,
                "meridian": meridian,
                "functions": functions,
                "active_compounds": active_compounds,
                "data_level": "enriched"
            })
        for i in range(0, len(data), 50):
            client.table("tcm_single_herb").upsert(
                data[i:i+50], on_conflict="name_cn").execute()
        print(f"Supabase同步完成: {len(data)} 条")

if __name__ == "__main__":
    enrich_properties()
