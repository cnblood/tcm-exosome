def run():
    client = create_client(SUPABASE_URL, SUPABASE_KEY)

    # Step1: 更新成方制剂基本信息
    print("Step1: 更新成方制剂信息...")
    updated = 0
    for name_cn, (indications, key_herbs, functions) in FORMULA_INFO.items():
        try:
            client.table("tcm_compound_formula").update({
                "indications": indications,
                "key_herbs": key_herbs,
                "data_level": "enriched"
            }).eq("name_cn", name_cn).execute()
            updated += 1
        except Exception as e:
            print(f"  Error {name_cn}: {e}")
    print(f"  Updated {updated} formulas")

    # Step2: 建formula_gene_relations表（直接insert，Supabase会自动处理）
    print("Step2: 写入复方-基因关联...")

    all_relations = []
    for formula_name, relations in FORMULA_GENE_RELATIONS.items():
        for gene, itype, mechanism, score in relations:
            all_relations.append({
                "formula_name": formula_name,
                "gene_symbol": gene,
                "interaction_type": itype,
                "mechanism": mechanism,
                "confidence_score": score,
            })

    # 分批写入
    inserted = 0
    for i in range(0, len(all_relations), 50):
        batch = all_relations[i:i+50]
        try:
            client.table("formula_gene_relations").upsert(
                batch, on_conflict="formula_name,gene_symbol"
            ).execute()
            inserted += len(batch)
            print(f"  Inserted {inserted}/{len(all_relations)}...")
        except Exception as e:
            # 表不存在时提示
            print(f"  Error: {e}")
            print("  请先在Supabase SQL Editor运行建表SQL")
            return

    print(f"\nDone!")
    print(f"  Formulas enriched: {updated}")
    print(f"  Gene relations inserted: {inserted}")
    print(f"  Formulas covered: {len(FORMULA_GENE_RELATIONS)}")

    # 统计
    res = client.table("formula_gene_relations").select("id", count="exact").execute()
    print(f"  Total in DB: {res.count}")

if __name__ == "__main__":
    run()
