def get_pinyin_initial(name):
    """获取药名首字拼音首字母"""
    if not name: return ''
    first = name[0]
    return PINYIN_INITIAL.get(first, first.upper())

def get_pinyin_abbr(name):
    """获取每个字的拼音首字母缩写（用于搜索）"""
    return ''.join(PINYIN_INITIAL.get(c, '') for c in name if '\u4e00' <= c <= '\u9fff')

