def run():
    client = create_client(SUPABASE_URL, SUPABASE_KEY)

    print("Step1: 写入中药-疾病关联...")
    disease_data = []
    for herb, relations in HERB_DISEASE.items():
        for disease, effect, mechanism, score, evidence in relations:
            disease_data.append({
                "herb_name": herb,
                "disease": disease,
                "effect": effect,
                "mechanism": mechanism,
                "confidence_score": score,
                "evidence_level": evidence,
            })
    inserted_d = 0
    for i in range(0, len(disease_data), 50):
        client.table("herb_disease_relations").upsert(
            disease_data[i:i+50], on_conflict="herb_name,disease"
        ).execute()
        inserted_d += len(disease_data[i:i+50])
        print(f"  {inserted_d}/{len(disease_data)}...")
    print(f"  Done: {inserted_d} relations")

    print("Step2: 写入中药-通路关联...")
    pathway_data = []
    for herb, relations in HERB_PATHWAY.items():
        for pathway, pid, regulation, genes, score in relations:
            pathway_data.append({
                "herb_name": herb,
                "pathway_name": pathway,
                "pathway_id": pid,
                "regulation": regulation,
                "key_genes": genes,
                "confidence_score": score,
            })
    inserted_p = 0
    for i in range(0, len(pathway_data), 50):
        client.table("herb_pathway_relations").upsert(
            pathway_data[i:i+50], on_conflict="herb_name,pathway_name"
        ).execute()
        inserted_p += len(pathway_data[i:i+50])
        print(f"  {inserted_p}/{len(pathway_data)}...")
    print(f"  Done: {inserted_p} relations")

    # 统计总量
    total_d = client.table("herb_disease_relations").select("id", count="exact").execute()
    total_p = client.table("herb_pathway_relations").select("id", count="exact").execute()
    print(f"\n完成！")
    print(f"  本次新增 - 疾病: {inserted_d}, 通路: {inserted_p}")
    print(f"  数据库总量 - 疾病: {total_d.count}, 通路: {total_p.count}")
    print(f"  覆盖中药 - 疾病: {len(HERB_DISEASE)+16}, 通路: {len(HERB_PATHWAY)+16} (含已有16味)")

if __name__ == "__main__":
    run()
