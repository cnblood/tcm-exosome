def run():
    if not SUPABASE_URL or not SUPABASE_KEY:
        print("需要设置SUPABASE_URL和SUPABASE_KEY")
        return

    client = create_client(SUPABASE_URL, SUPABASE_KEY)

    data = []
    for name_cn, (nature, flavor, meridian, functions, active_compounds) in HERB_PROPERTIES.items():
        data.append({
            "name_cn": name_cn,
            "nature": nature,
            "flavor": flavor,
            "meridian": meridian,
            "functions": functions,
            "active_compounds": active_compounds,
            "data_level": "enriched"
        })

    # 分批upsert
    updated = 0
    for i in range(0, len(data), 50):
        batch = data[i:i+50]
        client.table("tcm_single_herb").upsert(batch, on_conflict="name_cn").execute()
        updated += len(batch)
        print(f"  Synced {updated}/{len(data)}...")

    print(f"\nSupabase synced: {len(data)} herbs")

    # 统计
    res = client.table("tcm_single_herb").select("data_level", count="exact").eq("data_level", "enriched").execute()
    print(f"Total enriched: {res.count}")

if __name__ == "__main__":
    run()
